  Future<void> _loadUser() async {
    try {
      final session = AuthRepository.instance.current;
      if (session == null) {
        setState(() {
          _isLoading = false;
          _loadError = 'Сначала войдите в систему';
        });
        return;
      }
      final users = await UsersTable().queryRows(
        queryFn: (q) => q.eqOrNull('user_id', session.userId),
      );
      _currentUser = users.firstOrNull;
      setState(() {
        _isLoading = false;
        _loadError = null;
      });
    } catch (e) {
      setState(() {
        _isLoading = false;
        _loadError = 'Ошибка загрузки данных';
      });
    }
  }
